package com.dest.collectionframework.arraylist;

public class wrapperClasses {
	public static void main(String[] args) {
		Byte b = new Byte((byte)123);
		System.out.println(b);
		Short s = new Short((short)3000);
		System.out.println(s);
		Integer i = new Integer(46000);
		System.out.println(i);
		Long l = new Long(23456789);
		System.out.println(l);
		Float f = new Float(2345.6789);
		System.out.println(f);
		Double d = new Double(45678.9875);
		System.out.println(d);
		Character c = new Character('L');
		System.out.println(c);
		String str = new String("Nani");
		System.out.println(str);
		Boolean B = new Boolean(false);
		System.out.println(B);


	}
}
